package subtracttwonumbers;

/**
 *
 * @author Mr. Pataracchia
 */
public class SubtractTwoNumbers { // Start class

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) { // Start main method
        // Declare variables.
        double value1;
        double value2;
        double answer;
        // Initialize variables.
        value1 = 34.1;
        value2 = 12.3;
        // Calculate difference and store the result in answer.
        answer = value1 - value2;
        // Output result (answer) to the NetBeans console.
        System.out.println ("The second number subtracted from the first number equals: " + answer);

    } // End of main method.
}// End of class.
